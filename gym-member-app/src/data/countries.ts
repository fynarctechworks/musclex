/** Max national digits for a country (defaults to 15). */
export function maxDigitsFor(code: string): number {
  return PHONE_MAX_DIGITS[code] ?? 15;
}

/**
 * Exact national number length for a known country (e.g. India → 10), or null
 * when we don't have a fixed length and should accept a 6–15 range instead.
 */
export function expectedDigitsFor(code: string): number | null {
  return PHONE_MAX_DIGITS[code] ?? null;
}

/**
 * Whether `length` digits form a complete national number for the country.
 * Known countries require their exact length; unknown ones accept 6–15.
 */
export function isPhoneComplete(code: string, length: number): boolean {
  const exact = expectedDigitsFor(code);
  return exact !== null ? length === exact : length >= 6 && length <= 15;
}
