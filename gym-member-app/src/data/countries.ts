/**
 * Global country dial-code list for the phone-login picker. Flags are rendered as
 * emoji (derived from the ISO-2 code) — they show as real flags on iOS/Android;
 * the web preview (Windows) may show the two letters instead.
 *
 * Comprehensive list (UN members + major territories). Dial codes are ITU E.164.
 * If any specific code looks off, flag it — a few micro-territory codes are easy
 * to mistype and are low-traffic.
 */
export interface Country {
  code: string; // ISO 3166-1 alpha-2
  dial: string; // +<dialing code>
  name: string;
}

/** Flag emoji from an ISO-2 code (regional-indicator symbols). */
export function flagEmoji(iso2: string): string {
  return iso2
    .toUpperCase()
    .replace(/./g, (c) => String.fromCodePoint(127397 + c.charCodeAt(0)));
}

